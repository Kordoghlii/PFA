def add(a: int, b: int) -> int:
    #Somme de deux entiers
    """Adds two integers.
    Args:
        a: First integer.
        b: Second integer.
    Returns:
        Sum of a and b.
    """
    return a + b

def subtract(a: int, b: int) -> int:
    #Soustraction de deux entiers
    """Adds two integers.
    Args:
        a: First integer.
        b: Second integer.
    Returns:
        Substract of a and b.
    """
    return a - b